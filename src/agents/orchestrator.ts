  private calculateProgress(): number {
    const phases = ['requirements_analysis', 'architecture_design', 'development', 'testing', 'deployment', 'complete'];
    const currentPhaseIndex = phases.indexOf(this.context.phase);
    return Math.round(((currentPhaseIndex + 1) / phases.length) * 100);
  }

  private getNextPhase(currentPhase: ProjectPhase): ProjectPhase | null {
    const phases = ['requirements_analysis', 'architecture_design', 'development', 'testing', 'deployment', 'complete'];
    const currentIndex = phases.indexOf(currentPhase);
    return currentIndex < phases.length - 1 ? phases[currentIndex + 1] as ProjectPhase : null;
  }

  private estimateCompletion(progress: number): string {
    const remainingWork = 100 - progress;
    const estimatedHours = Math.ceil(remainingWork / 10); // Rough estimate
    return `${estimatedHours} hours remaining`;
  }

