  private async handleProjectError(error: Error): Promise<void> {
    this.logger.error('Project error occurred', { error: error.message });
    
    // Create error report
    const errorReport = {
      timestamp: new Date().toISOString(),
      error: error.message,
      stack: error.stack,
      phase: this.context.phase,
      context: this.context,
    };

    const errorPath = `${this.context.id}/docs`;
    await this.fs.ensureDirectory(errorPath);
    await this.fs.writeFile(`${errorPath}/error-report.json`, JSON.stringify(errorReport, null, 2));
  }

  private async initializeGit(): Promise<void> {
    if (config.git.enabled) {
      try {
        await this.git.initialize();
        this.logger.info('Git repository initialized for project orchestration');
      } catch (error) {
        this.logger.warn('Failed to initialize Git repository', { error });
      }
    }
  }
