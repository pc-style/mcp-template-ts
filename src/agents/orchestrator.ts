  protected getRoleResponsibilities(): string {
    return `- Coordinate and manage the autonomous development team
- Orchestrate the development workflow from PRD to prototype
- Assign tasks to appropriate agents based on project phase
- Monitor progress and ensure quality gates are met
- Manage project context and shared state
- Facilitate communication between agents
- Ensure project completion and delivery
- Handle errors and recovery procedures`;
  }

  async execute(task: AgentTask): Promise<any> {
    this.logTaskStart(task);

    try {
      switch (task.title.toLowerCase()) {
        case 'orchestrate_project':
          return await this.orchestrateProject(task);
        case 'manage_workflow':
          return await this.manageWorkflow(task);
        case 'assign_tasks':
          return await this.assignTasks(task);
        case 'monitor_progress':
          return await this.monitorProgress(task);
        case 'validate_phase':
          return await this.validatePhase(task);
        case 'handle_error':
          return await this.handleError(task);
        case 'generate_project_summary':
          return await this.generateProjectSummary(task);
        default:
          throw new Error(`Unknown task: ${task.title}`);
      }
    } catch (error) {
      this.logTaskError(task, error as Error);
      throw error;
    }
  }

  private async orchestrateProject(task: AgentTask): Promise<{ project_result: any }> {
    const prdContent = task.result?.prd_content;
    const projectName = task.result?.project_name || 'Auto-Generated Project';
    const projectDescription = task.result?.project_description || 'Project generated by MCP Dev Team';

    if (!prdContent) {
      throw new Error('PRD content is required for project orchestration');
    }

    this.logger.info('Starting project orchestration', { projectName });

    // Initialize project context
    const projectContext: ProjectContext = {
      id: `project_${Date.now()}`,
      name: projectName,
      description: projectDescription,
      phase: 'requirements_analysis',
      requirements: [],
      user_stories: [],
      decisions: [],
      artifacts: [],
    };

    this.updateContext(projectContext);

    // Initialize git repository
    await this.git.initialize(projectContext.id);

    try {
      // Phase 1: Requirements Analysis (Product Manager)
      this.logger.info('Phase 1: Requirements Analysis');
      await this.executePhase('requirements_analysis', prdContent);

      // Phase 2: Architecture Design (Tech Lead)
      this.logger.info('Phase 2: Architecture Design');
      await this.executePhase('architecture_design');

      // Phase 3: Development (Developer)
      this.logger.info('Phase 3: Development');
      await this.executePhase('development');

      // Phase 4: Testing (QA Engineer)
      this.logger.info('Phase 4: Testing');
      await this.executePhase('testing');

      // Phase 5: Deployment (DevOps Engineer)
      this.logger.info('Phase 5: Deployment');
      await this.executePhase('deployment');

      // Phase 6: Project Completion
      this.logger.info('Phase 6: Project Completion');
      await this.executePhase('complete');

      // Generate final project summary
      const projectSummary = await this.generateFinalProjectSummary();

      this.logger.info('Project orchestration completed successfully', { projectName });

      return { project_result: projectSummary };

    } catch (error) {
      this.logger.error('Project orchestration failed', { error, projectName });
      await this.handleProjectError(error as Error);
      throw error;
    }
  }

  private async executePhase(phase: ProjectPhase, prdContent?: string): Promise<void> {
    this.updateContext({ phase });

    switch (phase) {
      case 'requirements_analysis':
        await this.executeRequirementsPhase(prdContent!);
        break;
      case 'architecture_design':
        await this.executeArchitecturePhase();
        break;
      case 'development':
        await this.executeDevelopmentPhase();
        break;
      case 'testing':
        await this.executeTestingPhase();
        break;
      case 'deployment':
        await this.executeDeploymentPhase();
        break;
      case 'complete':
        await this.executeCompletionPhase();
        break;
    }

    // Validate phase completion
    await this.validatePhaseCompletion(phase);
  }

