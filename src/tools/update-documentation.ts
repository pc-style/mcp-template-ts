import { z } from 'zod';
import { FileOperations } from '../utils/file-operations.js';

export const updateDocumentationSchema = z.object({
  projectPath: z.string().describe('Path to the project directory'),
  documentationType: z.enum(['readme', 'api', 'architecture', 'deployment', 'contributing', 'changelog']).describe('Type of documentation to update'),
  content: z.object({
    title: z.string().optional(),
    sections: z.array(z.object({
      heading: z.string(),
      content: z.string(),
      subsections: z.array(z.object({
        heading: z.string(),
        content: z.string()
      })).optional()
    }))
  }).optional().describe('Structured content for the documentation'),
  markdownContent: z.string().optional().describe('Raw markdown content to write'),
  appendMode: z.boolean().optional().describe('Whether to append to existing documentation'),
  autoGenerate: z.boolean().optional().describe('Whether to auto-generate documentation from code')
});

export async function updateDocumentation(args: z.infer<typeof updateDocumentationSchema>) {
  try {
    let finalContent = '';
    let filePath = '';

    // Determine file path based on documentation type
    switch (args.documentationType) {
      case 'readme':
        filePath = `${args.projectPath}/README.md`;
        break;
      case 'api':
        filePath = `${args.projectPath}/docs/API.md`;
        break;
      case 'architecture':
        filePath = `${args.projectPath}/docs/ARCHITECTURE.md`;
        break;
      case 'deployment':
        filePath = `${args.projectPath}/docs/DEPLOYMENT.md`;
        break;
      case 'contributing':
        filePath = `${args.projectPath}/CONTRIBUTING.md`;
        break;
      case 'changelog':
        filePath = `${args.projectPath}/CHANGELOG.md`;
        break;
    }

    // Generate content based on input
    if (args.markdownContent) {
      finalContent = args.markdownContent;
    } else if (args.content) {
      finalContent = generateMarkdownFromStructure(args.content, args.documentationType);
    } else if (args.autoGenerate) {
      finalContent = await autoGenerateDocumentation(args.projectPath, args.documentationType);
    } else {
      return {
        content: [
          {
            type: "text",
            text: JSON.stringify({
              success: false,
              error: 'No content provided for documentation update'
            }, null, 2)
          }
        ]
      };
    }

    // Handle append mode
    if (args.appendMode && await FileOperations.fileExists(filePath)) {
      const existingContent = await FileOperations.readFile(filePath);
      finalContent = existingContent + '\n\n' + finalContent;
    }

    // Write the documentation file
    await FileOperations.writeFile(filePath, finalContent);

    return {
      content: [
        {
          type: "text",
          text: JSON.stringify({
            success: true,
            message: `${args.documentationType} documentation updated successfully`,
            data: {
              filePath,
              documentationType: args.documentationType,
              size: finalContent.length,
              mode: args.appendMode ? 'append' : 'replace',
              preview: finalContent.substring(0, 200) + (finalContent.length > 200 ? '...' : '')
            }
          }, null, 2)
        }
      ]
    };
  } catch (error) {
    return {
      content: [
        {
          type: "text",
          text: JSON.stringify({
            success: false,
            error: `Documentation update failed: ${error.message}`
          }, null, 2)
        }
      ]
    };
  }
}

function generateMarkdownFromStructure(content: any, documentationType: string): string {
  let markdown = '';

  if (content.title) {
    markdown += `# ${content.title}\n\n`;
  }

  content.sections.forEach((section: any) => {
    markdown += `## ${section.heading}\n\n`;
    markdown += `${section.content}\n\n`;

    if (section.subsections) {
      section.subsections.forEach((subsection: any) => {
        markdown += `### ${subsection.heading}\n\n`;
        markdown += `${subsection.content}\n\n`;
      });
    }
  });

  return markdown;
}

async function autoGenerateDocumentation(projectPath: string, documentationType: string): Promise<string> {
  switch (documentationType) {
    case 'readme':
      return await generateReadmeDocumentation(projectPath);
    case 'api':
      return await generateApiDocumentation(projectPath);
    case 'architecture':
      return await generateArchitectureDocumentation(projectPath);
    case 'deployment':
      return await generateDeploymentDocumentation(projectPath);
    case 'contributing':
      return generateContributingDocumentation();
    case 'changelog':
      return generateChangelogDocumentation();
    default:
      return `# ${documentationType.charAt(0).toUpperCase() + documentationType.slice(1)} Documentation\n\nDocumentation content goes here.\n`;
  }
}

async function generateReadmeDocumentation(projectPath: string): Promise<string> {
  // Read package.json for project info
  let projectInfo: any = {};
  try {
    const packageJsonPath = `${projectPath}/package.json`;
    if (await FileOperations.fileExists(packageJsonPath)) {
      const packageContent = await FileOperations.readFile(packageJsonPath);
      projectInfo = JSON.parse(packageContent);
    }
  } catch (error) {
    // Ignore package.json parsing errors
  }

  const projectName = projectInfo.name || 'Project';
  const description = projectInfo.description || 'A generated project';

  return `# ${projectName}

## Description

${description}

## Installation

\`\`\`bash
npm install
\`\`\`

## Usage

\`\`\`bash
# Development
npm run dev

# Build
npm run build

# Start
npm start

# Test
npm test
\`\`\`

## Scripts

${projectInfo.scripts ? Object.entries(projectInfo.scripts).map(([name, script]) => 
  `- \`npm run ${name}\`: ${script}`
).join('\n') : 'No scripts defined'}

## Dependencies

### Production Dependencies
${projectInfo.dependencies ? Object.keys(projectInfo.dependencies).map(dep => `- ${dep}`).join('\n') : 'None'}

### Development Dependencies
${projectInfo.devDependencies ? Object.keys(projectInfo.devDependencies).map(dep => `- ${dep}`).join('\n') : 'None'}

## Contributing

Please read CONTRIBUTING.md for details on our code of conduct and the process for submitting pull requests.

## License

${projectInfo.license || 'No license specified'}
`;
}

async function generateApiDocumentation(projectPath: string): Promise<string> {
  // Scan for API routes and controllers
  const apiFiles = await findApiFiles(projectPath);
  
  let documentation = `# API Documentation

## Overview

This document describes the API endpoints available in this application.

## Base URL

\`\`\`
http://localhost:3000/api
\`\`\`

## Authentication

Most endpoints require authentication via JWT token in the Authorization header:

\`\`\`
Authorization: Bearer <your-jwt-token>
\`\`\`

## Endpoints

`;

  // Extract endpoints from files
  for (const file of apiFiles) {
    try {
      const content = await FileOperations.readFile(file);
      const endpoints = extractEndpoints(content);
      
      if (endpoints.length > 0) {
        documentation += `### ${file.split('/').pop()}\n\n`;
        endpoints.forEach(endpoint => {
          documentation += `#### ${endpoint.method} ${endpoint.path}\n\n`;
          documentation += `${endpoint.description}\n\n`;
          
          if (endpoint.parameters.length > 0) {
            documentation += `**Parameters:**\n`;
            endpoint.parameters.forEach(param => {
              documentation += `- \`${param.name}\` (${param.type}): ${param.description}\n`;
            });
            documentation += '\n';
          }
          
          documentation += `**Response:**\n\`\`\`json\n${endpoint.response}\n\`\`\`\n\n`;
        });
      }
    } catch (error) {
      // Skip files that can't be read
    }
  }

  return documentation;
}

