async function findApiFiles(projectPath: string): Promise<string[]> {
  const apiFiles: string[] = [];
  
  try {
    // Look for common API file patterns
    const searchPaths = [
      `${projectPath}/src/controllers`,
      `${projectPath}/src/routes`,
      `${projectPath}/src/api`,
      `${projectPath}/controllers`,
      `${projectPath}/routes`,
      `${projectPath}/api`
    ];

    for (const searchPath of searchPaths) {
      const files = await FileOperations.listFiles(searchPath, true);
      const jsFiles = files.filter(file => 
        file.endsWith('.js') || file.endsWith('.ts') || file.endsWith('.jsx') || file.endsWith('.tsx')
      );
      apiFiles.push(...jsFiles);
    }
  } catch (error) {
    // Directory doesn't exist, continue
  }

  return apiFiles;
}

function extractEndpoints(content: string): any[] {
  const endpoints: any[] = [];
  
  // Simple regex patterns to extract API endpoints
  const routePatterns = [
    /router\.(get|post|put|delete|patch)\s*\(\s*['"`]([^'"`]+)['"`]/g,
    /app\.(get|post|put|delete|patch)\s*\(\s*['"`]([^'"`]+)['"`]/g,
    /@(Get|Post|Put|Delete|Patch)\s*\(\s*['"`]([^'"`]+)['"`]/g
  ];

  routePatterns.forEach(pattern => {
    let match;
    while ((match = pattern.exec(content)) !== null) {
      endpoints.push({
        method: match[1].toUpperCase(),
        path: match[2],
        description: `API endpoint for ${match[2]}`,
        parameters: [],
        response: '{\n  "success": true,\n  "data": {}\n}'
      });
    }
  });

  return endpoints;
}